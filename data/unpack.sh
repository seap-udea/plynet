#!/bin/bash
rundir=$(dirname $0)
echo "Untar data..."
tar xf $rundir/pack.tar -C $rundir
echo "Gathering data..."
cat $rundir/pack/data_* > /tmp/data.tgz
echo "Unpacking data into data directory..."
tar zxf /tmp/data.tgz -C $rundir
